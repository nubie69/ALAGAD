// Language detection and translation functions
export const detectLanguage = (text) => {
  const lowerText = text.toLowerCase();
  
  // Cebuano keywords
  const cebuanoWords = ['maayo', 'asa', 'unsaon', 'diin', 'kampus', 'building', 'opisin'];
  if (cebuanoWords.some(word => lowerText.includes(word))) {
    return 'ceb';
  }
  
  // Tagalog keywords
  const tagalogWords = ['kamusta', 'nasaan', 'paano', 'saan', 'kampus', 'gusali', 'opisina'];
  if (tagalogWords.some(word => lowerText.includes(word))) {
    return 'tl';
  }
  
  // Default to English
  return 'en';
};

export const translate = (key, language = 'en') => {
  return translations[language]?.[key] || translations.en[key] || key;
};

export const getTranslations = (language = 'en') => {
  return translations[language] || translations.en;
};

export default translations;